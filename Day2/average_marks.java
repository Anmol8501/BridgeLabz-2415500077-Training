public class average_marks {    
    public static void main(String[] args) {
        int marks1 = 94; // Marks in subject 1
        int marks2 = 95; // Marks in subject 2
        int marks3 = 96; // Marks in subject 3
        int totalMarks = marks1 + marks2 + marks3; // Calculate total marks
        double average = totalMarks / 3.0; // Calculate average
        System.out.println("Sam's average mark in PCM is  " + average);
    }
    
}
